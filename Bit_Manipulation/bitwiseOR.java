package Bit_Manipulation;

public class bitwiseOR {
    public static void main(String[] args) {
        int a = 19;
        int b = 10;
        int c = 35;
        System.out.println(a|b|c);
        System.out.println(a&b);
        System.out.println(a&b&c);
        
        System.out.println(3<<8);
    }
}
